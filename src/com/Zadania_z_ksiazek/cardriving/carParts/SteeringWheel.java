package com.Zadania_z_ksiazek.cardriving.carParts;

public class SteeringWheel {
    double maximumAngleOfSteeringWheel;
    double diameter;
    double lowering;

    public SteeringWheel(double maximumAngleOfSteeringWheel, double diameter, double lowering) {
        this.maximumAngleOfSteeringWheel = maximumAngleOfSteeringWheel;
        this.diameter = diameter;
        this.lowering = lowering;
    }
}
