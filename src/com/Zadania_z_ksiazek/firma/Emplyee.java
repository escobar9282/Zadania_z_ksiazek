package com.Zadania_z_ksiazek.firma;

public class Emplyee {
    int age;
    String firstName;
    String lastName;
    long ID;
    int dateOfBirth;
    int experienceYears;

    public Emplyee(int age, String firstName, String lastName, long ID, int dateOfBirth, int experienceYears) {
        this.age = age;
        this.firstName = firstName;
        this.lastName = lastName;
        this.ID = ID;
        this.dateOfBirth = dateOfBirth;
        this.experienceYears = experienceYears;
    }
}
